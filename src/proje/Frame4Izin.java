    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int secilenSatir = TableIzin.getSelectedRow();
        if(secilenSatir == -1){
            JOptionPane.showMessageDialog(null, "Lütfen bir seçim yapın");
        }
        else{
            try {
                String tc =TableIzin.getValueAt(secilenSatir, 0).toString();
                int izin = (int) TableIzin.getValueAt(secilenSatir, 3) + ComboBoxEkle.getSelectedIndex() - ComboBoxSil.getSelectedIndex();
                String sorgu = "UPDATE personel SET izin = " + izin + "WHERE tc ='"+tc+"'";

                Connection con = Connecter.connect();
                Statement st = con.createStatement();
                st.executeLargeUpdate(sorgu);
                con.close();
                IzinGoruntule();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "İzin güncelleme hatası");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void TableIzinMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableIzinMousePressed
        int select = TableIzin.getSelectedRow();
        int izin = (int) TableIzin.getValueAt(select, 3);
        int sonuc = (izin*100)/30;
        
        Thread t = new Thread(){
            public void run(){
                for (int i = 0; i < sonuc +1; i++) {
                    try {
                        TimeUnit.MILLISECONDS.sleep(25);
                        
                    } catch (InterruptedException ex) {
                        Logger.getLogger(Frame4Izin.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    ProgressBar.setValue(i);
    
                }
                
            }
    
        };
        t.start();
            
        
    }//GEN-LAST:event_TableIzinMousePressed

