    public void IzinGoruntule(){
        TableIzin.setModel(new javax.swing.table.DefaultTableModel(
            new Object [][] {
                {null, null, null, null},
                {null, null, null, null},
                {null, null, null, null},
                {null, null, null, null}
            },
            new String [] {
                "TC", "AD", "SOYAD", "KULLANILMIŞ İZİN"
            }
        ));
        try {
            Connection con = Connecter.connect();
            Statement st = con.createStatement();
            ResultSet sonuc = st.executeQuery("SELECT * FROM personel");
            while(sonuc.next()){
                Object[] satir = new Object[4]; //sütun sayısı kadar satır objesi oluştu
                for (int i = 1; i <= 3; i++) {  //datalar tabloya yerleştiriliyor
                    satir[i-1] = sonuc.getObject(i);
                }
                satir[3] = sonuc.getObject(6);
                ((DefaultTableModel) TableIzin.getModel()).insertRow(sonuc.getRow() - 1, satir);
            }
            con.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Bağlantı hatası İzin");
        }
    }

